package com.example.wdgfarm_android.model;

public class Company {

    private int code;
    private String name;

    public Company (){

    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
