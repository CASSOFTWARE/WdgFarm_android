package com.example.wdgfarm_android.model;

public class Box {

    private String name;
    private float weight;

    public Box (){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

}
